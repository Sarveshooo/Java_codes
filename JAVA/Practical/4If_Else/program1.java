class Range{
	
	public static void main(String[] args){
		
		int num = 0;
		
		if(num == 0){
		
			System.out.println("number is zeo");
		}else{

			if(num>=1 && num<= 1000){
		
				System.out.println(num +" is in range of 1 to 1000");	
			}else{
		
				System.out.println(num +" is not in range of 1 to 1000");
			}	
		}
	}
}
